  private def writeMetadata(
      resourceDir: java.io.File,
      sortedServices: List[(String, String)],
      protocols: Map[String, String]
  ): File = {
    val entries = sortedServices.map { case (name, version) =>
      val protocol = protocols.getOrElse(name, "unknown")
      ObjectNode
        .builder()
        .withMember("name", name)
        .withMember("version", version)
        .withMember("protocol", protocol)
        .build()
    }

    val servicesArray = Node.arrayNode(entries: _*)
    val root = ObjectNode
      .builder()
      .withMember("services", servicesArray)
      .build()

    val content = Node.prettyPrintJson(root)

    val target = resourceDir / "aws-service-metadata.json"
    sbt.IO.write(target, content)
    target
  }

  private def fetchServiceVersions(): Map[String, String] = {
    val bomFiles = coursierapi.Fetch
      .create()
      .addDependencies(
        coursierapi.Dependency.of(
          Dependencies.AwsModels.org,
          "all",
          Dependencies.AwsModels.bomVersion
        )
      )
      .addArtifactTypes("pom")
      .fetch()
      .asScala
      .toList

    val pomFile = bomFiles.find(_.getName.endsWith(".pom")).getOrElse {
      throw new MessageOnlyException(
        "Failed to resolve AWS models BOM POM from Maven Central"
      )
    }

    val dbFactory = javax.xml.parsers.DocumentBuilderFactory.newInstance()
    dbFactory.setNamespaceAware(true)
    val doc = dbFactory.newDocumentBuilder().parse(pomFile)
    doc.getDocumentElement().normalize()
    val pomNs = "http://maven.apache.org/POM/4.0.0"

    val dmList = doc.getElementsByTagNameNS(pomNs, "dependencyManagement")
    if (dmList.getLength == 0) Map.empty[String, String]
    else {
      val dm = dmList.item(0).asInstanceOf[org.w3c.dom.Element]
      val depList = dm.getElementsByTagNameNS(pomNs, "dependency")
      (0 until depList.getLength).map { i =>
        val dep = depList.item(i).asInstanceOf[org.w3c.dom.Element]
        val artifactId =
          dep.getElementsByTagNameNS(pomNs, "artifactId").item(0).getTextContent
        val version =
          dep.getElementsByTagNameNS(pomNs, "version").item(0).getTextContent
        (artifactId, version)
      }.toMap
    }
  }

  private def resolveProtocols(
      services: List[(String, String)],
      cacheDir: java.io.File
  ): Map[String, String] = {
    val cacheFile = new java.io.File(cacheDir, ".aws-protocol-cache")
    readProtocolCache(cacheFile) match {
      case Some(protocols) => protocols
      case None =>
        val protocols = resolveProtocolsFromJars(services)
        writeProtocolCache(cacheFile, protocols)
        protocols
    }
  }

  private def resolveProtocolsFromJars(
      services: List[(String, String)]
  ): Map[String, String] = {
    val protocolKeys = List(
      "aws.protocols#restJson1" -> "restJson1",
      "aws.protocols#restXml" -> "restXml",
      "aws.protocols#awsJson1_0" -> "awsJson1_0",
      "aws.protocols#awsJson1_1" -> "awsJson1_1",
      "aws.protocols#awsQuery" -> "awsQuery",
      "aws.protocols#ec2Query" -> "ec2Query"
    )

    services.map { case (name, version) =>
      val protocol = resolveServiceJar(name, version) match {
        case Some(jar) => detectProtocol(jar, protocolKeys)
        case None      => "unknown"
      }
      (name, protocol)
    }.toMap
  }

  /** Resolve the service model JAR for a single AWS service.
    * Each service resolves independently so a missing artifact
    * doesn't prevent other services from being detected.
    */
  private def resolveServiceJar(
      name: String,
      version: String
  ): Option[java.io.File] = {
    try {
      val jars = coursierapi.Fetch
        .create()
        .addDependencies(
          coursierapi.Dependency.of(Dependencies.AwsModels.org, name, version)
        )
        .fetch()
        .asScala
        .toList
      // The service's own JAR contains the artifact name as its filename prefix.
      // Transitive deps (smithy-aws-traits, etc.) have different prefixes, so matching
      // within this per-service scope is unambiguous.
      jars.find { jar =>
        val fileName = jar.getName
        fileName.startsWith(name + "-") || fileName.startsWith(
          name.replace('-', '_') + "-"
        )
      }
    } catch {
      case e: Exception =>
        System.err.println(
          s"[smithy4s] Warning: failed to resolve AWS model $name:$version: ${e.getMessage}"
        )
        None
    }
  }

  private def readProtocolCache(
      cacheFile: java.io.File
  ): Option[Map[String, String]] = {
    if (!cacheFile.exists()) None
    else
      Using(Source.fromFile(cacheFile)) { source =>
        val lines = source.getLines().toList
        val cachedVersion = lines.headOption.getOrElse("")
        if (cachedVersion != Dependencies.AwsModels.bomVersion) None
        else
          Some(
            lines.tail
              .filter(_.contains("="))
              .map { line =>
                val idx = line.indexOf('=')
                (line.substring(0, idx), line.substring(idx + 1))
              }
              .toMap
          )
      }.toOption.flatten
  }

  private def writeProtocolCache(
      cacheFile: java.io.File,
      protocols: Map[String, String]
  ): Unit = {
    val header = Dependencies.AwsModels.bomVersion
    val entries = protocols.toSeq.sortBy(_._1).map { case (name, protocol) =>
      s"$name=$protocol"
    }
    val content = s"$header\n${entries.mkString("\n")}\n"
    cacheFile.getParentFile.mkdirs()
    sbt.IO.write(cacheFile, content)
  }

  private def detectProtocol(
      jar: java.io.File,
      protocolKeys: List[(String, String)]
  ): String = {
    Using.resource(new java.util.zip.ZipFile(jar)) { zipFile =>
      val smithyEntries = zipFile.entries().asScala.toList.filter { entry =>
        val name = entry.getName
        name.startsWith("META-INF/smithy/") &&
        name.endsWith(".json") &&
        !name.contains("manifest")
      }

      protocolKeys
        .find { case (key, _) =>
          smithyEntries.exists { entry =>
            Using.resource(zipFile.getInputStream(entry)) { is =>
              Source.fromInputStream(is, "UTF-8").mkString.contains(key)
            }
          }
        }
        .map(_._2)
        .getOrElse("unknown")
    }
  }

  private def toCamelCase(str: String): String = {
    str.split('-').toList match {
      case head :: tl => (head :: tl.map(_.capitalize)).mkString
      case Nil        => ""
    }
  }

}
