import sbt._
import scala.jdk.CollectionConverters._
import scala.io.Source
import scala.util.Using
import software.amazon.smithy.model.node.{Node, ObjectNode}

object AwsBoilerplate {

  private lazy val cachedServiceVersions: Map[String, String] =
    fetchServiceVersions()

  def generateSources(sourceDir: java.io.File): File = {
    val sortedServices = cachedServiceVersions.toList.sortBy(_._1)
    writeSpecs(sourceDir, sortedServices)
  }

  def generateResources(resourceDir: java.io.File): File = {
    val sortedServices = cachedServiceVersions.toList.sortBy(_._1)
    val protocols = resolveProtocols(sortedServices, resourceDir)
    writeMetadata(resourceDir, sortedServices, protocols)
  }

